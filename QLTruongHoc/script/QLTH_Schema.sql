-- DROP ROLE
CREATE OR REPLACE PROCEDURE SYS.DROP_ROLE
    (ROLE_NAME VARCHAR2)
AS
    COUNT_VAR NUMBER;
    STRSQL VARCHAR2(2000);
BEGIN
    STRSQL := 'ALTER SESSION SET "_ORACLE_SCRIPT" = TRUE';
    EXECUTE IMMEDIATE (STRSQL);
    
    STRSQL := 'SELECT COUNT(*) FROM DBA_ROLES WHERE DBA_ROLES.ROLE = ' || '''' || ROLE_NAME || '''';
    EXECUTE IMMEDIATE (STRSQL) INTO COUNT_VAR;
    
    IF (COUNT_VAR = 1) THEN
        STRSQL := 'DROP ROLE' || ' "' || ROLE_NAME || '"';
        EXECUTE IMMEDIATE (STRSQL);
    END IF;
    
    STRSQL := 'ALTER SESSION SET "_ORACLE_SCRIPT" = FALSE';
    EXECUTE IMMEDIATE (STRSQL);
END;
